/**
  * @brief This function handles USART1 global interrupt.
  */
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
  uint8_t rcv_data = 0;
  if(__HAL_UART_GET_FLAG(&huart1, UART_FLAG_RXNE) != RESET) {
    HAL_UART_Receive(&huart1,&rcv_data,1,1000);
    if(g_uart1_rev_len < UART1_REV_BUF_LEN) {
      g_uart1_rcv_buf[g_uart1_rev_len] = rcv_data;
      g_uart1_rev_len++;
    }
    __HAL_UART_CLEAR_FLAG(&huart1, UART_FLAG_RXNE);
  }
  if(__HAL_UART_GET_FLAG(&huart1, UART_FLAG_IDLE) != RESET) {
     uart1_rcv_callback(g_uart1_rcv_buf, g_uart1_rev_len);
     memset(g_uart1_rcv_buf, 0, UART1_REV_BUF_LEN);
     g_uart1_rev_len = 0;
    __HAL_UART_CLEAR_IDLEFLAG(&huart1);
  }
  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */

  /* USER CODE END USART1_IRQn 1 */
}

